import { spawn } from 'child_process';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import os from 'os';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

let isRunning = false;

// ‚úÖ ADAPTABLE: Detecta autom√°ticamente el entorno
const isVPS = os.platform() === 'linux' && fs.existsSync('/root');
const WORKDIR = isVPS ? '/root/superbot1.0' : __dirname;
const LOG_FILE = path.join(WORKDIR, 'cron_ejecuciones.log');

const INTERVAL = 17 * 60 * 1000; // 17 minutos
const MAX_ERRORS_PER_SCRIPT = 15; // ‚úÖ NUEVO: M√°ximo de errores antes de saltar al siguiente
const SCRIPTS = [
  'clientify/runner_clientify.js',
  'meta/fetch_meta_ads.js', 
  'pruebarun.js'
];

// ‚úÖ NUEVO: Contador de errores por script
const errorCounters = {};
// ‚úÖ NUEVO: Registro de problemas para JSON
const problemLog = {
  timestamp: null,
  batch_info: {},
  scripts: {},
  summary: {
    total_errors: 0,
    failed_scripts: [],
    successful_scripts: [],
    skipped_scripts: []
  }
};

// ‚úÖ MEJORADO: Crear directorio si no existe
function ensureLogDirectory() {
  const logDir = path.dirname(LOG_FILE);
  if (!fs.existsSync(logDir)) {
    fs.mkdirSync(logDir, { recursive: true });
  }
}

/** Agrega una l√≠nea al log */
function log(line) {
  const stamp = `[${new Date().toISOString()}] `;
  try {
    ensureLogDirectory();
    fs.appendFileSync(LOG_FILE, stamp + line + '\n');
    console.log(stamp + line); // Tambi√©n mostrar en consola
  } catch (error) {
    console.error('Error escribiendo al log:', error.message);
    console.log(stamp + line); // Fallback a consola
  }
}

/** ‚úÖ NUEVO: Resetea el contador de errores para un script */
function resetErrorCounter(script) {
  errorCounters[script] = 0;
  log(`üîÑ Contador de errores reseteado para ${script}`);
  
  // ‚úÖ NUEVO: Actualizar log de problemas
  if (problemLog.scripts[script]) {
    problemLog.scripts[script].status = 'recovered';
    problemLog.scripts[script].last_success = new Date().toISOString();
  }
}

/** ‚úÖ NUEVO: Incrementa el contador de errores para un script */
function incrementErrorCounter(script, errorMessage = '') {
  if (!errorCounters[script]) {
    errorCounters[script] = 0;
  }
  errorCounters[script]++;
  log(`‚ùå Error ${errorCounters[script]}/${MAX_ERRORS_PER_SCRIPT} para ${script}`);
  
  // ‚úÖ NUEVO: Registrar error en problemLog
  if (!problemLog.scripts[script]) {
    problemLog.scripts[script] = {
      total_errors: 0,
      errors: [],
      status: 'running',
      first_error: null,
      last_error: null
    };
  }
  
  const errorEntry = {
    timestamp: new Date().toISOString(),
    error_count: errorCounters[script],
    message: errorMessage,
    type: errorMessage.includes('ENOENT') ? 'file_not_found' : 
           errorMessage.includes('504') ? 'server_timeout' :
           errorMessage.includes('500') ? 'server_error' : 'unknown'
  };
  
  problemLog.scripts[script].total_errors++;
  problemLog.scripts[script].errors.push(errorEntry);
  problemLog.scripts[script].last_error = new Date().toISOString();
  problemLog.scripts[script].status = 'failing';
  
  if (!problemLog.scripts[script].first_error) {
    problemLog.scripts[script].first_error = new Date().toISOString();
  }
  
  problemLog.summary.total_errors++;
  
  return errorCounters[script];
}

/** ‚úÖ NUEVO: Verifica si un script ha alcanzado el l√≠mite de errores */
function hasReachedErrorLimit(script) {
  return errorCounters[script] >= MAX_ERRORS_PER_SCRIPT;
}

/** ‚úÖ NUEVO: Guarda el reporte de problemas en JSON */
function saveProblemReport() {
  try {
    const reportPath = path.join(WORKDIR, 'error_report.json');
    
    // Actualizar summary
    problemLog.summary.failed_scripts = Object.keys(problemLog.scripts).filter(
      script => problemLog.scripts[script].status === 'failing' || problemLog.scripts[script].status === 'skipped'
    );
    problemLog.summary.successful_scripts = Object.keys(problemLog.scripts).filter(
      script => problemLog.scripts[script].status === 'success' || problemLog.scripts[script].status === 'recovered'
    );
    problemLog.summary.skipped_scripts = Object.keys(problemLog.scripts).filter(
      script => problemLog.scripts[script].status === 'skipped'
    );
    
    // ‚úÖ MODIFICADO: Siempre guardar el reporte (sobreescribir cada vez)
    fs.writeFileSync(reportPath, JSON.stringify(problemLog, null, 2));
    
    if (problemLog.summary.total_errors > 0) {
      log(`üìÑ Reporte de errores guardado en: ${reportPath} (${problemLog.summary.total_errors} errores)`);
    } else {
      log(`üìÑ Reporte de batch exitoso guardado en: ${reportPath}`);
    }
  } catch (error) {
    log(`‚ùå Error al guardar reporte: ${error.message}`);
  }
}

/** Ejecuta un script hijo con manejo de errores mejorado */
function runScript(script) {
  return new Promise((resolve, reject) => {
    log(`‚ñ∂Ô∏è Ejecutando ${script} (errores: ${errorCounters[script] || 0}/${MAX_ERRORS_PER_SCRIPT})`);
    const scriptPath = path.join(WORKDIR, script);
    
    // ‚úÖ VALIDACI√ìN: Verificar que el script existe
    if (!fs.existsSync(scriptPath)) {
      const error = `Script no encontrado: ${scriptPath}`;
      log(`‚ùå ${error}`);
      incrementErrorCounter(script);
      reject(new Error(error));
      return;
    }
    
    const child = spawn('node', [scriptPath], { stdio: 'inherit' });

    child.on('close', (code) => {
      if (code === 0) {
        log(`‚úÖ Finaliz√≥ ${script}`);
        resetErrorCounter(script); // ‚úÖ NUEVO: Resetear contador en √©xito
        
        // ‚úÖ NUEVO: Marcar como exitoso en problemLog
        if (!problemLog.scripts[script]) {
          problemLog.scripts[script] = {};
        }
        problemLog.scripts[script].status = 'success';
        problemLog.scripts[script].last_success = new Date().toISOString();
        
        resolve();
      } else {
        const errorMessage = `Script ${script} termin√≥ con c√≥digo ${code}`;
        const errorCount = incrementErrorCounter(script, errorMessage);
        log(`‚ùå Error en ${script} ‚Äì exit ${code}`);
        
        // ‚úÖ NUEVO: Verificar l√≠mite de errores
        if (hasReachedErrorLimit(script)) {
          log(`üö® ${script} ha alcanzado el l√≠mite de ${MAX_ERRORS_PER_SCRIPT} errores consecutivos`);
          log(`‚è≠Ô∏è Saltando al siguiente script...`);
          
          // ‚úÖ NUEVO: Marcar como saltado
          problemLog.scripts[script].status = 'skipped';
          problemLog.scripts[script].skipped_at = new Date().toISOString();
          
          resetErrorCounter(script); // Resetear para la pr√≥xima vez
          resolve(); // ‚úÖ CAMBIO: Resolver en lugar de rechazar
        } else {
          reject(new Error(errorMessage));
        }
      }
    });

    // ‚úÖ NUEVO: Manejar errores de spawn
    child.on('error', (error) => {
      const errorMessage = `Error al ejecutar ${script}: ${error.message}`;
      const errorCount = incrementErrorCounter(script, errorMessage);
      log(`‚ùå ${errorMessage}`);
      
      if (hasReachedErrorLimit(script)) {
        log(`üö® ${script} ha alcanzado el l√≠mite de ${MAX_ERRORS_PER_SCRIPT} errores consecutivos`);
        log(`‚è≠Ô∏è Saltando al siguiente script...`);
        
        // ‚úÖ NUEVO: Marcar como saltado
        problemLog.scripts[script].status = 'skipped';
        problemLog.scripts[script].skipped_at = new Date().toISOString();
        
        resetErrorCounter(script);
        resolve();
      } else {
        reject(error);
      }
    });
  });
}

/** ‚úÖ MEJORADO: Ejecuta un script con reintentos autom√°ticos */
async function runScriptWithRetry(script) {
  while (!hasReachedErrorLimit(script)) {
    try {
      await runScript(script);
      return; // √âxito, salir del bucle
    } catch (error) {
      log(`üîÑ Reintentando ${script} en 5 segundos...`);
      await new Promise(resolve => setTimeout(resolve, 5000)); // Esperar 5 segundos
    }
  }
  
  log(`‚è≠Ô∏è L√≠mite de errores alcanzado para ${script}, continuando con el siguiente`);
}

async function runBatch() {
  if (isRunning) {
    log('‚è≥ Batch anterior a√∫n en ejecuci√≥n. Omitiendo...');
    return;
  }

  isRunning = true;
  
  // ‚úÖ NUEVO: Reinicializar reporte de problemas para cada batch
  problemLog.timestamp = new Date().toISOString();
  problemLog.batch_info = {
    environment: isVPS ? 'VPS' : 'Windows',
    workdir: WORKDIR,
    start_time: new Date().toISOString(),
    scripts_to_run: SCRIPTS,
    max_errors_per_script: MAX_ERRORS_PER_SCRIPT
  };
  // ‚úÖ NUEVO: Limpiar errores anteriores
  problemLog.scripts = {};
  problemLog.summary = {
    total_errors: 0,
    failed_scripts: [],
    successful_scripts: [],
    skipped_scripts: []
  };
  
  log(`üöÄ Iniciando batch en ${isVPS ? 'VPS' : 'Windows'} - WORKDIR: ${WORKDIR}`);
  
  try {
    // ‚úÖ MEJORADO: Ejecutar todos los scripts con reintentos
    for (const script of SCRIPTS) {
      log(`üìã Procesando script: ${script}`);
      await runScriptWithRetry(script);
      log(`‚úÖ Script ${script} completado o saltado`);
    }
    
    // Notificar a Apps Script solo si todo sali√≥ bien (si existe la funci√≥n)
    if (typeof notifyAppScript === 'function') {
      const result = await notifyAppScript();
      log(`üì§ Notificaci√≥n exitosa: ${JSON.stringify(result)}`);
    }
    
    // ‚úÖ NUEVO: Actualizar informaci√≥n final del batch
    problemLog.batch_info.end_time = new Date().toISOString();
    problemLog.batch_info.duration_ms = new Date() - new Date(problemLog.batch_info.start_time);
    
    // ‚úÖ NUEVO: Guardar reporte si hay problemas
    saveProblemReport();
    
    log('üéâ Batch completa');
  } catch (err) {
    log(`üí• Error en batch: ${err.message}`);
    
    // ‚úÖ NUEVO: Registrar error general del batch
    problemLog.batch_info.batch_error = {
      message: err.message,
      timestamp: new Date().toISOString()
    };
    saveProblemReport();
  } finally {
    isRunning = false;
  }
}

async function main() {
  log(`üéØ Runner iniciado en ${os.platform()} - ${isVPS ? 'Modo VPS' : 'Modo desarrollo'}`);
  log(`‚öôÔ∏è Configuraci√≥n: M√°ximo ${MAX_ERRORS_PER_SCRIPT} errores por script antes de saltar al siguiente`);
  
  // ‚úÖ NUEVO: Inicializar contadores
  SCRIPTS.forEach(script => {
    errorCounters[script] = 0;
  });
  
  while (true) {
    await runBatch();
    log(`‚è≥ Esperando ${INTERVAL / 60000} minutos para la pr√≥xima tanda...`);
    await new Promise(res => setTimeout(res, INTERVAL));
  }
}

main();